void fft2(MatrixXcd &C, const MatrixXcd &Y) {
    int m = Y.rows();
    int n = Y.cols();
    C.resize(m, n);
    MatrixXcd tempMatrix(m, n);
    Eigen::FFT<double> fft;

    for (int i = 0; i < m; ++i) {
        VectorXcd temp = Y.row(i);
        tempMatrix.row(i) = fft.fwd(temp).transpose();

    }

    for (int j = 0; j < n; ++j) {
        VectorXcd temp = tempMatrix.col(j);
        C.col(j) = fft.fwd(temp);

    }

}

void ifft2(MatrixXcd &C, const MatrixXcd &Y) {

    int m = Y.rows();
    int n = Y.cols();
    fft2(C, Y.conjugate());
    C = C.conjugate() / (m * n);


}

void conv2(MatrixXcd &C, const MatrixXcd &A1, const MatrixXcd &A2) {


    int n1 = A1.rows();
    int n2 = A2.rows();
    int m1 = A1.cols();
    int m2 = A2.cols();
    int rows = n1 + n2 - 1;
    int cols = m1 + m2 - 1;

    MatrixXcd tmp1 = MatrixXcd::Zero(rows, cols);
    MatrixXcd tmp2 = MatrixXcd::Zero(rows, cols);

    MatrixXcd A1New = MatrixXcd::Zero(rows, cols);
    MatrixXcd A2_New = MatrixXcd::Zero(rows, cols);


    A1New.topLeftCorner(n1, m1) = A1;
    A2_New.topLeftCorner(n2, m2) = A2;

    fft2(tmp1, A1New);
    fft2(tmp2, A2_New);
    auto matrix = tmp1.cwiseProduct(tmp2);
    ifft2(C, matrix);


}


